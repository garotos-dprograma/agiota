    private void btnlblEntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnlblEntrarMouseClicked
        this.conectar.conectaBanco();
        boolean sucesso = false;
        try{
            String query = "SELECT "
                    + "id, "
                    + "nome, "
                    + "agiota "
                    + "FROM pessoa "
                    + "WHERE email = '" + this.txtEmail.getText() + "' "
                    + "AND senha = '" + this.pwdSenha.getText() + "';";
            this.conectar.executarSQL(query);
            
            while (this.conectar.getResultSet().next()) {
                usuarioLogin.setId(this.conectar.getResultSet().getInt(1));
                usuarioLogin.setNome(this.conectar.getResultSet().getString(2));
                usuarioLogin.setAgiota(this.conectar.getResultSet().getBoolean(3));
            }
            if (usuarioLogin.getNome().equals("")){
                JOptionPane.showMessageDialog(null, "UsuÃ¡rio ou senha incorretos!");
            } else {
                sucesso = true;
            }
        } catch (Exception e) {
            System.out.println("Erro ao efetuar login! " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao efetuar login!");
            
        } finally {
            setUsuarioLogado(this.usuarioLogin);
            this.conectar.fechaBanco();
            if(sucesso){
                if(this.usuarioLogin.getAgiota()){
                    new MenuAgiota().setVisible(true);
                } else {
                    new MenuDevedor().setVisible(true);
                }
                Login.this.dispose();
            }
        }
    }//GEN-LAST:event_btnlblEntrarMouseClicked
    
